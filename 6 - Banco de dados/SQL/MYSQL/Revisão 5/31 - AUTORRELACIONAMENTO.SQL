# CRIAR TABELA DE AUTORELACIONAMENTO
CREATE TABLE FUNCIONARIOS (
    IDFUNCIONARIO INT PRIMARY KEY AUTO_INCREMENT,
    NOME VARCHAR(32) NOT NULL,
    CARGO VARCHAR(32) NOT NULL,
    ## FK DO AUTORRELACIONAMENTO
    GESTOR INT
);

# CONSTRAINT DE AUTORRELACIONAMENTO
ALTER TABLE FUNCIONARIOS
ADD CONSTRAINT FK_GESTORFUNCIONARIOS_IDFUNCIONARIOS
FOREIGN KEY(GESTOR) REFERENCES FUNCIONARIOS(IDFUNCIONARIO);

# INNER JOIN NO AUTORELACIONAMENTO
SELECT F.NOME, F.CARGO, GESTOR.NOME AS GESTOR 
FROM FUNCIONARIOS F 
INNER JOIN FUNCIONARIOS AS GESTOR ON F.GESTOR = GESTOR.IDFUNCIONARIO;